let year = "2024";

console.log("year", year);
console.log("typeof year", typeof year);
console.log("(year + 1) =", (year + 1));

// Manully Type casting (Converting) from String to Number.
console.log("Converting from String to Number");
year = Number(year);
console.log("year", year);
console.log("typeof year", typeof year);
console.log("(year + 1) =", (year + 1));

// Manully Type casting (Converting) from Number to String.
console.log("Converting from Number to String");
year = String(year);
console.log("year", year);
console.log("typeof year", typeof year);
console.log("(year + 1) =", (year + 1));

