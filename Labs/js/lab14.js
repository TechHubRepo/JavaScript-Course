let input = prompt("Enter The Day of week");


console.log("DAY OF WEEK", input);

switch(input){
    case "1":
        alert("MONDAY");
    break;
    case "2":
        alert("TUESDAY");
    break;
    case "3":
        alert("WEDNESSDAY");
    break;
    case "4":
        alert("THURSDAY");
    break;
    case "5":
        alert("FRIDAY");
    break;
    case "6":
        alert("SATURDAY");
    break;
    case "7":
        alert("SUNDAY");
    break;
    default:
        alert("INVALID INPUT")
    break

}
